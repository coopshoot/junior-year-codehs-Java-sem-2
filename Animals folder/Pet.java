public class Pet extends Animal
{
    private String name;
    private String size;
    
    public String getName()
    {
        return name;
    }
    public void setName(String x)
    {
        name = x;
    }
    public String getSize()
    {
        return size;
    }
    public void setSize(String r)
    {
        size = r;
    }
   
}
