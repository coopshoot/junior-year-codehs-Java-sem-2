public class Computer
{
    private int memory;
    private int screenSize;
    
    public int getMemory()
    {
        return memory;
    }
    
    public void setMemory(int x)
    {
        memory = x;
    }
    
    public int getScreenSize()
    {
        return screenSize;
    }
    
    public void setScreenSize(int j)
    {
        screenSize = j;
    }
    
}
